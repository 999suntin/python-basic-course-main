# Data type 資料類別
print('你好') # String 字串
print("hello") # String 字串

#數字
print(7) #Integer 整數 +ve/-ve
print(4.5) #Float 浮點數

# Arithmetic Operator 運算符號
print(7+7) # 加
print(7-7) # 減
print(7*7) # 乘
print(7/7) # 除
print(7%7) # 餘數
print(7**2) # 指數

# String concatenation (串連)
student = '梁浩賢'
print('你好'+student+'同學，歡迎參加Python入門課程。')

# F-String Python 3.6
print(f'你好{student}同學，歡迎參加Python入門課程。')

#特別字串符號
print("\"python\"") # Escape Character
print("開\n新一行") # 新一行 new line

# len字串功能 (length)
print( len('hello') )
print(len(student))